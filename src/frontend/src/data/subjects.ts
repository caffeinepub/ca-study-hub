export function getSubjectsByLevel(level: CA_Level): SubjectData[] {
  return CA_SUBJECTS.filter((s) => s.level === level);
}

export function getSubjectByCode(code: string): SubjectData | undefined {
  return CA_SUBJECTS.find((s) => s.code === code);
}

export function getAllSubjectNames(level: CA_Level): string[] {
  return getSubjectsByLevel(level).map((s) => s.name);
}

